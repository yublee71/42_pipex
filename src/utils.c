#include "pipex.h"

void	exit_with_error(const char *str, int errno)
{
	perror(str);
	exit(errno);
}

void	free_str_array(char **array)
{
	int	i;

	i = 0;
	while (array[i])
		free(array[i++]);
	free(array);
}
